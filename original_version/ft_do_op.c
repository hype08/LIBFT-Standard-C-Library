#include "libft.h"

void		ft_do_op(char *nb1, char *op, char *nb2)
{
	int	n1;
	int n2;
	int	r;

	n1 = atoi(nb1);
	n2 = atoi(nb2);
	r = 0;
	if (op[0] == '+')
		r = n1 + n2;
	else if (op[0] == '-')
		r = n1 - n2;
	else if (op[0] == '/' && n2 != 0)
		r = n1 / n2;
	else if (op[0] == '*')
		r = n1 * n2;
	else if (op[0] == '%' && n2 != 0)
		r = n1 % n2;
	ft_putnbr(r);
}
